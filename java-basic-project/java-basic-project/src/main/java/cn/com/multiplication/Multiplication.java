package cn.com.multiplication;

import java.io.IOException;

/**
 * 乘法器
 * @auther:90611
 * @date:2020/12/29
 */
public class Multiplication {
    /**
     * 两个整数相乘
     *
     * @param num1
     * @param num2
     * @return
     */
    public int intMultiplication(int num1, int num2) throws IOException {
        return num1 * num2;
    }

}
